    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        String catString = cmbCategoria.getSelectedItem().toString();
        if (catString.equals("")) {
            JOptionPane.showMessageDialog(this, "É necessário selecionar uma categoria!");
            return;
        }
        Categoria c = categoriaController.getReadId(Integer.parseInt(catString.split(" ")[0]));
        System.out.println(c);
        try {
                JasperReport relatorio = JasperCompileManager.compileReport(getClass().getResourceAsStream("/sispatri/report/relatorio_depreciacao_categoria.jrxml"));
                Map parametros = new HashMap();
                parametros.put("subTitulo", "Relatório Depreciação - " + c.getDescricao());
                parametros.put("cod_categoria", c.getId());

                // Executa relatoio
                JasperPrint impressao = JasperFillManager.fillReport(relatorio, parametros, ((SessionImpl)getSession()).connection());

                // Exibe resultado em video
                JasperViewer.viewReport(impressao, false);

            } catch (Exception e) {
               e.printStackTrace();
            }
            this.dispose();
    }//GEN-LAST:event_btnConfirmarActionPerformed

