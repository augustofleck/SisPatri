    /**
     * Adiciona as categorias no combo de categorias.
     */
    private void adicionaCategoriasCombo() {
        List<Categoria> list = categoriaController.getAlAtivos();
        if (!list.isEmpty()) {
            List<String> listString = new ArrayList<>();
            for (Categoria a : list) {
                listString.add(a.getId() + " " + a.getDescricao());
            }
            cmbCategoria.setModel(new DefaultComboBoxModel(listString.toArray()));
        }
        
    }

